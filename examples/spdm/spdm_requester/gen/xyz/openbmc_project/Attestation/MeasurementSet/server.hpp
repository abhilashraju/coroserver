#pragma once
#include <limits>
#include <map>
#include <sdbusplus/sdbus.hpp>
#include <sdbusplus/server.hpp>
#include <string>
#include <systemd/sd-bus.h>

#include <xyz/openbmc_project/Common/error.hpp>
#include <xyz/openbmc_project/Attestation/MeasurementSet/common.hpp>

namespace sdbusplus::server::xyz::openbmc_project::attestation
{

class MeasurementSet :
    public sdbusplus::common::xyz::openbmc_project::attestation::MeasurementSet
{
    public:
        /* Define all of the basic class operations:
         *     Not allowed:
         *         - Default constructor to avoid nullptrs.
         *         - Copy operations due to internal unique_ptr.
         *         - Move operations due to 'this' being registered as the
         *           'context' with sdbus.
         *     Allowed:
         *         - Destructor.
         */
        MeasurementSet() = delete;
        MeasurementSet(const MeasurementSet&) = delete;
        MeasurementSet& operator=(const MeasurementSet&) = delete;
        MeasurementSet(MeasurementSet&&) = delete;
        MeasurementSet& operator=(MeasurementSet&&) = delete;
        virtual ~MeasurementSet() = default;

        /** @brief Constructor to put object onto bus at a dbus path.
         *  @param[in] bus - Bus to attach to.
         *  @param[in] path - Path to attach at.
         */
        MeasurementSet(bus_t& bus, const char* path) :
            _xyz_openbmc_project_attestation_MeasurementSet_interface(
                bus, path, interface, _vtable, this),
            _sdbusplus_bus(bus) {}

        /** @brief Implementation for SPDMGetSignedMeasurements
         *  This method generates an SPDM cryptographic signed statement over the given nonce and measurements of the SPDM Responder.
         *
         *  @param[in] measurementIndices - An array of indices that identify the measurement blocks to sign.
         *  @param[in] nonce - A 32-byte hex-encoded string to be signed with the measurements. The value should be unique.
         *  @param[in] slotId - The slot identifier for the certificate containing the private key to generate the signature over the measurements.
         *
         *  @return certificate[sdbusplus::message::object_path] - Certificate corresponding to the SPDM slot identifier that can be used to validate the signature.
         *  @return hashingAlgorithm[std::string] - The hashing algorithm used for generating the cryptographic signed statement.
         *  @return publicKey[std::string] - A Privacy Enhanced Mail (PEM)-encoded public key that can be used to validate the signature.
         *  @return signedMeasurements[std::string] - Base64 encoded cryptographic signed statement generated by the signer.
         *  @return signingAlgorithm[std::string] - The asymmetric signing algorithm used for generating the cryptographic signed statement.
         *  @return version[std::string] - The SPDM version used by the SPDM Responder to generate the cryptographic signed statement.
         */
        virtual std::tuple<sdbusplus::message::object_path, std::string, std::string, std::string, std::string, std::string> spdmGetSignedMeasurements(
            std::vector<size_t> measurementIndices,
            std::string nonce,
            size_t slotId) = 0;
        /** @brief Implementation for ExchangeCertificate
         *  This method initiates an SPDM certificate exchange operation with the SPDM Responder to retrieve and store the CA certificate chain.
         *
         *  @return success[bool] - Indicates whether the certificate exchange operation completed successfully (true) or failed (false).
         *  @return certificateLocation[std::string] - The file system path where the retrieved CA certificate has been stored. Returns an empty string if the operation failed.
         */
        virtual std::tuple<bool, std::string> exchangeCertificate(
            ) = 0;



        /** @brief Emit interface added */
        void emit_added()
        {
            _xyz_openbmc_project_attestation_MeasurementSet_interface.emit_added();
        }

        /** @brief Emit interface removed */
        void emit_removed()
        {
            _xyz_openbmc_project_attestation_MeasurementSet_interface.emit_removed();
        }

        /** @return the bus instance */
        bus_t& get_bus()
        {
            return  _sdbusplus_bus;
        }

    private:
        /** @brief sd-bus callback for SPDMGetSignedMeasurements
         */
        static int _callback_SPDMGetSignedMeasurements(
            sd_bus_message*, void*, sd_bus_error*);
        /** @brief sd-bus callback for ExchangeCertificate
         */
        static int _callback_ExchangeCertificate(
            sd_bus_message*, void*, sd_bus_error*);

        static const vtable_t _vtable[];
        sdbusplus::server::interface_t
                _xyz_openbmc_project_attestation_MeasurementSet_interface;
        bus_t&  _sdbusplus_bus;

};

} // namespace sdbusplus::server::xyz::openbmc_project::attestation

