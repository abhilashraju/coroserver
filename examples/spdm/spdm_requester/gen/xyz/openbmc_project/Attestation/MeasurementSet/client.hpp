#include <variant>

#include <xyz/openbmc_project/Common/error.hpp>
#include <xyz/openbmc_project/Attestation/MeasurementSet/common.hpp>

namespace sdbusplus::client::xyz::openbmc_project::attestation
{

namespace details
{
// forward declaration
template <typename Client, typename Proxy>
class MeasurementSet;
} // namespace details

/** Alias class so we can use the client in both a client_t aggregation
 *  and individually.
 *
 *  sdbusplus::async::client_t<MeasurementSet>() or
 *  MeasurementSet() both construct an equivalent instance.
 */
template <typename Client = void, typename Proxy = void>
struct MeasurementSet :
    public std::conditional_t<std::is_void_v<Client>,
                              sdbusplus::async::client_t<details::MeasurementSet>,
                              details::MeasurementSet<Client, Proxy>>
{
    template <typename... Args>
    MeasurementSet(Args&&... args) :
        std::conditional_t<std::is_void_v<Client>,
                           sdbusplus::async::client_t<details::MeasurementSet>,
                           details::MeasurementSet<Client, Proxy>>(
            std::forward<Args>(args)...)
    {}
};

namespace details
{

template <typename Client, typename Proxy>
class MeasurementSet :
    public sdbusplus::common::xyz::openbmc_project::attestation::MeasurementSet,
    private sdbusplus::async::client::details::client_context_friend
{
  public:
    friend Client;
    template <typename, typename>
    friend struct sdbusplus::client::xyz::openbmc_project::attestation::MeasurementSet;

    // Delete default constructor as these should only be constructed
    // indirectly through sdbusplus::async::client_t.
    MeasurementSet() = delete;

    /** @brief SPDMGetSignedMeasurements
     *  This method generates an SPDM cryptographic signed statement over the given nonce and measurements of the SPDM Responder.
     *
     *  @param[in] measurementIndices - An array of indices that identify the measurement blocks to sign.
     *  @param[in] nonce - A 32-byte hex-encoded string to be signed with the measurements. The value should be unique.
     *  @param[in] slotId - The slot identifier for the certificate containing the private key to generate the signature over the measurements.
     *
     *  @return certificate[sdbusplus::message::object_path] - Certificate corresponding to the SPDM slot identifier that can be used to validate the signature.
     *  @return hashingAlgorithm[std::string] - The hashing algorithm used for generating the cryptographic signed statement.
     *  @return publicKey[std::string] - A Privacy Enhanced Mail (PEM)-encoded public key that can be used to validate the signature.
     *  @return signedMeasurements[std::string] - Base64 encoded cryptographic signed statement generated by the signer.
     *  @return signingAlgorithm[std::string] - The asymmetric signing algorithm used for generating the cryptographic signed statement.
     *  @return version[std::string] - The SPDM version used by the SPDM Responder to generate the cryptographic signed statement.
     */
    auto spdm_get_signed_measurements(std::vector<size_t> measurementIndices, std::string nonce, size_t slotId)
    {
        return proxy.template call<sdbusplus::message::object_path, std::string, std::string, std::string, std::string, std::string>(context(), "SPDMGetSignedMeasurements", measurementIndices, nonce, slotId);
    }

    /** @brief ExchangeCertificate
     *  This method initiates an SPDM certificate exchange operation with the SPDM Responder to retrieve and store the CA certificate chain.
     *
     *  @return success[bool] - Indicates whether the certificate exchange operation completed successfully (true) or failed (false).
     *  @return certificateLocation[std::string] - The file system path where the retrieved CA certificate has been stored. Returns an empty string if the operation failed.
     */
    auto exchange_certificate()
    {
        return proxy.template call<bool, std::string>(context(), "ExchangeCertificate");
    }



  private:
    // Conversion constructor from proxy used by client_t.
    explicit constexpr MeasurementSet(Proxy p) :
        proxy(p.interface(interface))
    {}

    sdbusplus::async::context& context()
    {
        return sdbusplus::async::client::details::client_context_friend::
            context<Client, MeasurementSet>(this);
    }

    decltype(std::declval<Proxy>().interface(interface)) proxy = {};
};

} // namespace details

} // namespace sdbusplus::client::xyz::openbmc_project::attestation

